

// Arithmetic Operators handdson practise 09/12/2024
let a = 10, b = 3;
console.log("Addition:", a + b);         
console.log("Subtraction:", a - b);    
console.log("Multiplication:", a * b);  
console.log("Division:", a / b);        
console.log("Modulus:", a % b);         
console.log("Exponentiation:", a ** b);

// Assignment Operators
let c = 5;
c += 3;  // Equivalent to c = c + 3
console.log("Assignment (+=):", c);
c *= 2;  // Equivalent to c = c * 2
console.log("Assignment (*=):", c); 
// Comparison Operators
console.log("Equal to (==):", a == b);        
console.log("Not equal to (!=):", a != b);     
console.log("Strict equal (===):", a === 10);  
console.log("Strict not equal (!==):", a !== "10"); 
console.log("Greater than (>):", a > b);       
console.log("Less than (<):", a < b);        
// Logical Operators
let x = true, y = false;
console.log("Logical AND (&&):", x && y); 
console.log("Logical NOT (!):", !x);   

// Bitwise Operators
console.log("Bitwise AND (&):", a & b); 
console.log("Bitwise OR (|):", a | b);  
console.log("Bitwise XOR (^):", a ^ b); 
console.log("Bitwise NOT (~):", ~a);    
console.log("Left Shift (<<):", a << 1); 
console.log("Right Shift (>>):", a >> 1); 

// Ternary Operator
let age = 20;
let result = age >= 18 ? "Adult" : "Minor";
console.log("Ternary Operator:", result); 

// Type Operators
console.log("Typeof Operator:", typeof a); 
console.log("Instanceof Operator:", [] instanceof Array); 

// String Operators
let str1 = "Hello", str2 = "World";
console.log("String Concatenation:", str1 + " " + str2); 

// Unary Operators
let num = 5;
console.log("Increment (++):", ++num); 
console.log("Decrement (--):", --num); 
console.log("Unary Plus (+):", +num); 
console.log("Unary Negation (-):", -num); 
